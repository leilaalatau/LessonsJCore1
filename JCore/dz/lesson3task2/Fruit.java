package lessonsJCore.JCore.dz.lesson3task2;

public class Fruit {
    private String name;
    private Integer addFruit;

    public Fruit(String name, Integer addFruit){
        this.name = name;
        this.addFruit = addFruit;
    }

    public String getName() {
        return name;
    }

    public Integer getAddFruit() {
        return addFruit;
    }
}
